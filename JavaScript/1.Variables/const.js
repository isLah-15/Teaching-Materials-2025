// calculate area of a circle using constant variable
let radius = 5; // 'radius' is a variable
const pie = 3.14; // 'pie' is a constant variable

const area = pie * radius * radius; // 'area' is a constant variable
console.log("Area of the circle is: " + area); // 78.5
